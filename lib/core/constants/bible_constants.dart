  static String getFullName(int id, {bool isSimplified = true}) {
    if (isSimplified) {
      return bookFullNamesSimplified[id] ?? "";
    } else {
      return bookFullNamesTraditional[id] ?? "";
    }
  }

  static String getShortName(int id, {bool isSimplified = true}) {
    if (isSimplified) {
      return bookShortNamesSimplified[id] ?? "";
    } else {
      return bookShortNamesTraditional[id] ?? "";
    }
  }

  // Deprecated support (optional, can remove later)
  static String getSimplifiedFullName(int id) =>
      getFullName(id, isSimplified: true);
  static String getSimplifiedShortName(int id) =>
      getShortName(id, isSimplified: true);

  static String getCategoryName(int bookId) {
    for (var category in categories) {
      if (category.bookIds.contains(bookId)) {
        return category.name;
      }
    }
    return "";
  }

